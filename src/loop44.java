public class loop44 {
    public static void main(String[] args) {
        char y ='A';
        for (char i=1; i<=3; i++)
        {
            for (int  j=1 ;j<=i ;j++)
            {
                System.out.print(y +" ");
                y++;
            }
            System.out.println();
        }
    }
}
